-- CHECKPERMISSION
-----------------------------------------------------------------------------------------------------------------------------------------
function cRP.checkPermission()
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		ilegalRace[user_id] = false
		if vRP.tryGetInventoryItem(user_id,"credential",1) then
			TriggerEvent("blipsystem:serviceEnter",source,"Corredor",48)
			ilegalRace[user_id] = true

			local policeResult = vRP.numPermission("Police")
			for k,v in pairs(policeResult) do
				async(function()
					TriggerClientEvent("Notify",v,"amarelo","Detectamos um corredor clandestino nas ruas.",5000)
					vRPC.playSound(v,"Beep_Red","DLC_HEIST_HACKING_SNAKE_SOUNDS")
				end)
			end
		end

		return true,ilegalRace[user_id]
	end

	return false
end
