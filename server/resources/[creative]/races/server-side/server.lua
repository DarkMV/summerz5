-- FINISHRACE
-----------------------------------------------------------------------------------------------------------------------------------------
function cRP.finishRace(id,points)
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		local vehName = vRPC.vehicleName(source)
		local consult = vRP.query("races/checkResult",{ raceid = id, user_id = parseInt(user_id) })
		if consult[1] then
			if parseInt(points) < parseInt(consult[1]["points"]) then
				vRP.execute("races/updateRecords",{ raceid = id, user_id = parseInt(user_id), vehicle = vehicleName(vehName), points = parseInt(points) })
			end
		else
			local identity = vRP.userIdentity(user_id)
			vRP.execute("races/insertRecords",{ raceid = id, user_id = parseInt(user_id), name = identity["name"].." "..identity["name2"], vehicle = vehicleName(vehName), points = parseInt(points) })
		end

		if ilegalRace[user_id] then
			local minValue,maxValue = Races[id]["payment"][1],Races[id]["payment"][1]
			vRP.generateItem(user_id,"dollars",math.random(minValue,maxValue),true)
			TriggerEvent("blipsystem:serviceExit",source)
			vRP.upgradeStress(user_id,10)
			ilegalRace[user_id] = nil
		end
	end
end
